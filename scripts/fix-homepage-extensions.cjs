const fs = require('fs');
const path = require('path');

const dir = path.resolve(__dirname, '../custom/homepages/v2');

if (!fs.existsSync(dir)) process.exit(0);

// Rename .js -> .cjs
fs.readdirSync(dir).forEach((file) => {
  if (file.endsWith('.js')) {
    const oldPath = path.join(dir, file);
    const newPath = path.join(dir, file.replace(/\.js$/, '.cjs'));
    fs.renameSync(oldPath, newPath);
    console.log(`Renamed ${file} -> ${file.replace(/\.js$/, '.cjs')}`);
  }
});

// Fix require paths inside .cjs files: './foo.js' -> './foo.cjs'
fs.readdirSync(dir).forEach((file) => {
  if (file.endsWith('.cjs')) {
    const filePath = path.join(dir, file);
    const content = fs.readFileSync(filePath, 'utf8');
    const fixed = content.replace(/require\((['"])(.+?)\.js\1\)/g, "require($1$2.cjs$1)");
    if (fixed !== content) {
      fs.writeFileSync(filePath, fixed);
      console.log(`Fixed require paths in ${file}`);
    }
  }
});

// Create a browser-safe ESM entry when the homepage repo only provides index.cjs.
const indexCjsPath = path.join(dir, 'index.cjs');
const indexTsPath = path.join(dir, 'index.ts');
if (fs.existsSync(indexCjsPath)) {
  const cjsEntry = fs.readFileSync(indexCjsPath, 'utf8');
  const matches = [...cjsEntry.matchAll(/^\s*([A-Za-z0-9_$-]+)\s*:\s*require\((['"])(.+?)\2\)/gm)];

  if (matches.length > 0) {
    const imports = matches
      .map((match) => `import ${match[1]} from '${match[3]}';`)
      .join('\n');
    const exports = matches.map((match) => `  ${match[1]},`).join('\n');
    const namedExports = matches.map((match) => `export { ${match[1]} };`).join('\n');
    const shim = `// Auto-generated by scripts/fix-homepage-extensions.cjs
${imports}

${namedExports}

export default {
${exports}
};
`;

    fs.writeFileSync(indexTsPath, shim);
    console.log('Created custom homepage ESM shim: index.ts');
  }
}
